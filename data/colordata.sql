DROP TABLE IF EXISTS colors;
CREATE TABLE colors(
    hex NVARCHAR,
    name NVARCHAR, 
    red SMALLINT,
    green SMALLINT,
    blue SMALLINT,
    family NVARCHAR,
    link NVARCHAR,
    source NVARCHAR,
    ID NVARCHAR,
    hue SINGLE,
    Saturation SINGLE,
    Luminance SINGLE,
    Cyan SINGLE,
    Magenta SINGLE,
    Yellow SINGLE,
    Key SINGLE);
